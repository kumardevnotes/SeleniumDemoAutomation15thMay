package com.inheritance;

/*If you declare a class with final keyword, none can inherit or extend your class*/
public final class FinalClassDemo {

	public void displayCityName() {
		System.out.println("NewYork");
	}
	
	protected void displayCountryName() {
		System.out.println("U.S.A");
	}
	
	public void displayCityName1() {
		System.out.println("NewYork");
	}
	
	private void displayCityName2() {
		System.out.println("NewYork");
	}

}
